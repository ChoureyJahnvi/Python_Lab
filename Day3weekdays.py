# Printing day from 7 days of week
# taking input from user as number
r= int(input("enter any number between 1 to 7"))
if r==1: print("monday")
elif r==2: print("tuesday")
elif r==3: print("wednesday")
elif r==4: print("thursday")
elif r==5: print("friday")
elif r==6: print("saturday")
elif r==7: print("sunday")
else:
    print("blah")



