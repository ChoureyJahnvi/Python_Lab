#lambda function is used to create anonymous function
#syntax : argrment(variables) : expression(thing u wish to evaluate)

x = lambda a: a *2 # x will contain the value of expression a
print("value of x is: ", x(5)) # giving the value of function

x = lambda a, b: a + b  # giving two argument a and b
print("The value for x", x(6, 2))  # giving the value in function




